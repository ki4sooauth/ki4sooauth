package com.gooagoo.mobile.entity.mobc03.transform;

import java.io.Serializable;

/**
 * 吆喝广场商家查询 
 */
public class Shoplist implements Serializable
{

	private static final long serialVersionUID = 1L;

	/** 商家id  */
	private String shopid = "";

	/** 商家图片url  */
	private String shopurl = "";

	/** 商家名称  */
	private String shopname = "";

	/** 关注数量  */
	private String attentionnum = "";

	/** 会员数量  */
	private String membernum = "";

	/**
	 * 设置商家id 
	 * @param shopid	商家id 
	 */
	public void setShopid(String shopid)
	{
		this.shopid = shopid != null ? shopid : "";
	}

	/**
	 * 获取商家id 
	 * @return	商家id 
	 */
	public String getShopid()
	{
		return this.shopid;
	}

	/**
	 * 设置商家图片url 
	 * @param shopurl	商家图片url 
	 */
	public void setShopurl(String shopurl)
	{
		this.shopurl = shopurl != null ? shopurl : "";
	}

	/**
	 * 获取商家图片url 
	 * @return	商家图片url 
	 */
	public String getShopurl()
	{
		return this.shopurl;
	}

	/**
	 * 设置商家名称 
	 * @param shopname	商家名称 
	 */
	public void setShopname(String shopname)
	{
		this.shopname = shopname != null ? shopname : "";
	}

	/**
	 * 获取商家名称 
	 * @return	商家名称 
	 */
	public String getShopname()
	{
		return this.shopname;
	}

	/**
	 * 设置关注数量 
	 * @param attentionnum	关注数量 
	 */
	public void setAttentionnum(String attentionnum)
	{
		this.attentionnum = attentionnum != null ? attentionnum : "";
	}

	/**
	 * 获取关注数量 
	 * @return	关注数量 
	 */
	public String getAttentionnum()
	{
		return this.attentionnum;
	}

	/**
	 * 设置会员数量 
	 * @param membernum	会员数量 
	 */
	public void setMembernum(String membernum)
	{
		this.membernum = membernum != null ? membernum : "";
	}

	/**
	 * 获取会员数量 
	 * @return	会员数量 
	 */
	public String getMembernum()
	{
		return this.membernum;
	}
}