package com.gooagoo.mobile.entity.mobg01.transform;

import java.io.Serializable;

/**
 *  购物计划商品分类 
 */
public class Goods implements Serializable
{

	private static final long serialVersionUID = 1L;

	/** 分类的id  */
	private String categoryid = "";

	/** 图标的文件，分类的id图片url  */
	private String picurl = "";

	/** 类别的名称（分类的id名称）  */
	private String categoryname = "";

	/**  当前类别的父类别的id，当此节点为-1时，返回picurl；否则，picurl为空  */
	private String parentcategoryid = "";

	/** 是否删除  */
	private String isdel = "";

	/** 最后一次修改时间  */
	private String ctimestamp = "";

	/**
	 * 设置分类的id 
	 * @param categoryid	分类的id 
	 */
	public void setCategoryid(String categoryid)
	{
		this.categoryid = categoryid != null ? categoryid : "";
	}

	/**
	 * 获取分类的id 
	 * @return	分类的id 
	 */
	public String getCategoryid()
	{
		return this.categoryid;
	}

	/**
	 * 设置图标的文件，分类的id图片url 
	 * @param picurl	图标的文件，分类的id图片url 
	 */
	public void setPicurl(String picurl)
	{
		this.picurl = picurl != null ? picurl : "";
	}

	/**
	 * 获取图标的文件，分类的id图片url 
	 * @return	图标的文件，分类的id图片url 
	 */
	public String getPicurl()
	{
		return this.picurl;
	}

	/**
	 * 设置类别的名称（分类的id名称） 
	 * @param categoryname	类别的名称（分类的id名称） 
	 */
	public void setCategoryname(String categoryname)
	{
		this.categoryname = categoryname != null ? categoryname : "";
	}

	/**
	 * 获取类别的名称（分类的id名称） 
	 * @return	类别的名称（分类的id名称） 
	 */
	public String getCategoryname()
	{
		return this.categoryname;
	}

	/**
	 * 设置 当前类别的父类别的id，当此节点为-1时，返回picurl；否则，picurl为空 
	 * @param parentcategoryid	 当前类别的父类别的id，当此节点为-1时，返回picurl；否则，picurl为空 
	 */
	public void setParentcategoryid(String parentcategoryid)
	{
		this.parentcategoryid = parentcategoryid != null ? parentcategoryid : "";
	}

	/**
	 * 获取 当前类别的父类别的id，当此节点为-1时，返回picurl；否则，picurl为空 
	 * @return	 当前类别的父类别的id，当此节点为-1时，返回picurl；否则，picurl为空 
	 */
	public String getParentcategoryid()
	{
		return this.parentcategoryid;
	}

	/**
	 * 设置是否删除 
	 * @param isdel	是否删除 
	 */
	public void setIsdel(String isdel)
	{
		this.isdel = isdel != null ? isdel : "";
	}

	/**
	 * 获取是否删除 
	 * @return	是否删除 
	 */
	public String getIsdel()
	{
		return this.isdel;
	}

	/**
	 * 设置最后一次修改时间 
	 * @param ctimestamp	最后一次修改时间 
	 */
	public void setCtimestamp(String ctimestamp)
	{
		this.ctimestamp = ctimestamp != null ? ctimestamp : "";
	}

	/**
	 * 获取最后一次修改时间 
	 * @return	最后一次修改时间 
	 */
	public String getCtimestamp()
	{
		return this.ctimestamp;
	}
}