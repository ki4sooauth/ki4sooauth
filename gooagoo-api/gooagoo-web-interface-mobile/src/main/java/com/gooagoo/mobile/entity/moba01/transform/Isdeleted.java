package com.gooagoo.mobile.entity.moba01.transform;

import java.io.Serializable;

/**
 *  已删除的会员卡信息 
 */
public class Isdeleted implements Serializable
{

	private static final long serialVersionUID = 1L;

	/**  已删除的会员卡字符串，多条用“逗号”分隔  */
	private String scardnostr = "";

	/** 用户会员卡总数（用户所拥有的所有商家的会员卡总和） */
	private String count = "";

	/**
	 * 设置 已删除的会员卡字符串，多条用“逗号”分隔 
	 * @param scardnostr	 已删除的会员卡字符串，多条用“逗号”分隔 
	 */
	public void setScardnostr(String scardnostr)
	{
		this.scardnostr = scardnostr != null ? scardnostr : "";
	}

	/**
	 * 获取 已删除的会员卡字符串，多条用“逗号”分隔 
	 * @return	 已删除的会员卡字符串，多条用“逗号”分隔 
	 */
	public String getScardnostr()
	{
		return this.scardnostr;
	}

	/**
	 * 设置用户会员卡总数（用户所拥有的所有商家的会员卡总和）
	 * @param count	用户会员卡总数（用户所拥有的所有商家的会员卡总和）
	 */
	public void setCount(String count)
	{
		this.count = count != null ? count : "";
	}

	/**
	 * 获取用户会员卡总数（用户所拥有的所有商家的会员卡总和）
	 * @return	用户会员卡总数（用户所拥有的所有商家的会员卡总和）
	 */
	public String getCount()
	{
		return this.count;
	}
}