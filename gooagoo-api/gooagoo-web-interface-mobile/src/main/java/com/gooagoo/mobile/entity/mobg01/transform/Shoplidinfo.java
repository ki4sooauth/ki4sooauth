package com.gooagoo.mobile.entity.mobg01.transform;

import java.io.Serializable;

/**
 *  实体店主lid 
 */
public class Shoplidinfo implements Serializable
{

	private static final long serialVersionUID = 1L;

	/** 商家编号  */
	private String shopid = "";

	/** LID基本信息(实体店的主LID)，完整LID的前6位表示商家或实体店，此处只保存完整LID的前6位  */
	private String lidbase = "";

	/** 实体店编号，每个实体店至少独享一个LID  */
	private String shopentityid = "";

	/** 是否删除  */
	private String isdel = "";

	/** 最后一次修改时间  */
	private String ctimestamp = "";

	/**
	 * 设置商家编号 
	 * @param shopid	商家编号 
	 */
	public void setShopid(String shopid)
	{
		this.shopid = shopid != null ? shopid : "";
	}

	/**
	 * 获取商家编号 
	 * @return	商家编号 
	 */
	public String getShopid()
	{
		return this.shopid;
	}

	/**
	 * 设置LID基本信息(实体店的主LID)，完整LID的前6位表示商家或实体店，此处只保存完整LID的前6位 
	 * @param lidbase	LID基本信息(实体店的主LID)，完整LID的前6位表示商家或实体店，此处只保存完整LID的前6位 
	 */
	public void setLidbase(String lidbase)
	{
		this.lidbase = lidbase != null ? lidbase : "";
	}

	/**
	 * 获取LID基本信息(实体店的主LID)，完整LID的前6位表示商家或实体店，此处只保存完整LID的前6位 
	 * @return	LID基本信息(实体店的主LID)，完整LID的前6位表示商家或实体店，此处只保存完整LID的前6位 
	 */
	public String getLidbase()
	{
		return this.lidbase;
	}

	/**
	 * 设置实体店编号，每个实体店至少独享一个LID 
	 * @param shopentityid	实体店编号，每个实体店至少独享一个LID 
	 */
	public void setShopentityid(String shopentityid)
	{
		this.shopentityid = shopentityid != null ? shopentityid : "";
	}

	/**
	 * 获取实体店编号，每个实体店至少独享一个LID 
	 * @return	实体店编号，每个实体店至少独享一个LID 
	 */
	public String getShopentityid()
	{
		return this.shopentityid;
	}

	/**
	 * 设置是否删除 
	 * @param isdel	是否删除 
	 */
	public void setIsdel(String isdel)
	{
		this.isdel = isdel != null ? isdel : "";
	}

	/**
	 * 获取是否删除 
	 * @return	是否删除 
	 */
	public String getIsdel()
	{
		return this.isdel;
	}

	/**
	 * 设置最后一次修改时间 
	 * @param ctimestamp	最后一次修改时间 
	 */
	public void setCtimestamp(String ctimestamp)
	{
		this.ctimestamp = ctimestamp != null ? ctimestamp : "";
	}

	/**
	 * 获取最后一次修改时间 
	 * @return	最后一次修改时间 
	 */
	public String getCtimestamp()
	{
		return this.ctimestamp;
	}
}