package com.gooagoo.authzserver.entity.business;

import java.io.Serializable;

/**
 * 应用基本信息
 * @author Administrator
 *
 */
public class OpenAppInfoBusiness implements Serializable
{
    public static final long serialVersionUID = 1L;
    private String appKey;//应用KEY
    private String appName;//应用名称
    private String appType;//应用类型
    private String appLabel;//应用标签
    private String appSecret;//应用密钥
    private String volume;//证书流量
    private String appStatus;//应用状态
    private String userId;//用户ID
    private String createDt;//应用创建时间
    private String updateDt;//应用修改时间

    public String getAppKey()
    {
        return this.appKey;
    }

    public void setAppKey(String appKey)
    {
        this.appKey = appKey;
    }

    public String getAppName()
    {
        return this.appName;
    }

    public void setAppName(String appName)
    {
        this.appName = appName;
    }

    public String getAppType()
    {
        return this.appType;
    }

    public void setAppType(String appType)
    {
        this.appType = appType;
    }

    public String getAppLabel()
    {
        return this.appLabel;
    }

    public void setAppLabel(String appLabel)
    {
        this.appLabel = appLabel;
    }

    public String getAppSecret()
    {
        return this.appSecret;
    }

    public void setAppSecret(String appSecret)
    {
        this.appSecret = appSecret;
    }

    public String getVolume()
    {
        return this.volume;
    }

    public void setVolume(String volume)
    {
        this.volume = volume;
    }

    public String getAppStatus()
    {
        return this.appStatus;
    }

    public void setAppStatus(String appStatus)
    {
        this.appStatus = appStatus;
    }

    public String getUserId()
    {
        return this.userId;
    }

    public void setUserId(String userId)
    {
        this.userId = userId;
    }

    public String getCreateDt()
    {
        return this.createDt;
    }

    public void setCreateDt(String createDt)
    {
        this.createDt = createDt;
    }

    public String getUpdateDt()
    {
        return this.updateDt;
    }

    public void setUpdateDt(String updateDt)
    {
        this.updateDt = updateDt;
    }

    @Override
    public String toString()
    {
        return "OpenAppInfoBusiness [appKey=" + this.appKey + ", appName=" + this.appName + ", appType=" + this.appType + ", appLabel=" + this.appLabel + ", appSecret=" + this.appSecret + ", volume=" + this.volume + ", appStatus=" + this.appStatus + ", userId=" + this.userId + ", createDt=" + this.createDt + ", updateDt=" + this.updateDt + "]";
    }
}
