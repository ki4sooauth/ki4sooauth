package com.gooagoo.mobile.entity.mobf11.transform;

import java.io.Serializable;

/**
 *  版本信息
 */
public class Versionlist implements Serializable
{

	private static final long serialVersionUID = 1L;

	/**  APP识别码  */
	private String appcode = "";

	/**  移动设备类型,1-Android ,2-IOS  */
	private String mobiletype = "";

	/** 版本号 */
	private String versioncode = "";

	/** 版本名称 */
	private String versionname = "";

	/** 所属平台 */
	private String platform = "";

	/**  备注  */
	private String note = "";

	/** Apk下载地址 */
	private String downloadurl = "";

	/**  创建时间  */
	private String createtime = "";

	/**
	 * 设置 APP识别码 
	 * @param appcode	 APP识别码 
	 */
	public void setAppcode(String appcode)
	{
		this.appcode = appcode != null ? appcode : "";
	}

	/**
	 * 获取 APP识别码 
	 * @return	 APP识别码 
	 */
	public String getAppcode()
	{
		return this.appcode;
	}

	/**
	 * 设置 移动设备类型,1-Android ,2-IOS 
	 * @param mobiletype	 移动设备类型,1-Android ,2-IOS 
	 */
	public void setMobiletype(String mobiletype)
	{
		this.mobiletype = mobiletype != null ? mobiletype : "";
	}

	/**
	 * 获取 移动设备类型,1-Android ,2-IOS 
	 * @return	 移动设备类型,1-Android ,2-IOS 
	 */
	public String getMobiletype()
	{
		return this.mobiletype;
	}

	/**
	 * 设置版本号
	 * @param versioncode	版本号
	 */
	public void setVersioncode(String versioncode)
	{
		this.versioncode = versioncode != null ? versioncode : "";
	}

	/**
	 * 获取版本号
	 * @return	版本号
	 */
	public String getVersioncode()
	{
		return this.versioncode;
	}

	/**
	 * 设置版本名称
	 * @param versionname	版本名称
	 */
	public void setVersionname(String versionname)
	{
		this.versionname = versionname != null ? versionname : "";
	}

	/**
	 * 获取版本名称
	 * @return	版本名称
	 */
	public String getVersionname()
	{
		return this.versionname;
	}

	/**
	 * 设置所属平台
	 * @param platform	所属平台
	 */
	public void setPlatform(String platform)
	{
		this.platform = platform != null ? platform : "";
	}

	/**
	 * 获取所属平台
	 * @return	所属平台
	 */
	public String getPlatform()
	{
		return this.platform;
	}

	/**
	 * 设置 备注 
	 * @param note	 备注 
	 */
	public void setNote(String note)
	{
		this.note = note != null ? note : "";
	}

	/**
	 * 获取 备注 
	 * @return	 备注 
	 */
	public String getNote()
	{
		return this.note;
	}

	/**
	 * 设置Apk下载地址
	 * @param downloadurl	Apk下载地址
	 */
	public void setDownloadurl(String downloadurl)
	{
		this.downloadurl = downloadurl != null ? downloadurl : "";
	}

	/**
	 * 获取Apk下载地址
	 * @return	Apk下载地址
	 */
	public String getDownloadurl()
	{
		return this.downloadurl;
	}

	/**
	 * 设置 创建时间 
	 * @param createtime	 创建时间 
	 */
	public void setCreatetime(String createtime)
	{
		this.createtime = createtime != null ? createtime : "";
	}

	/**
	 * 获取 创建时间 
	 * @return	 创建时间 
	 */
	public String getCreatetime()
	{
		return this.createtime;
	}
}