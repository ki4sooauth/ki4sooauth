package com.gooagoo.mobile.entity.mobp01.transform;

import java.io.Serializable;

/**
 *  用户发票抬头信息
 */
public class Receiptinfo implements Serializable
{

	private static final long serialVersionUID = 1L;

	/**  发票信息编号，UUID */
	private String receiptid = "";

	/**  公司名称  */
	private String companyname = "";

	/** 是否默认选项，Y-是，N-否 */
	private String isdefault = "";

	/**
	 * 设置 发票信息编号，UUID
	 * @param receiptid	 发票信息编号，UUID
	 */
	public void setReceiptid(String receiptid)
	{
		this.receiptid = receiptid != null ? receiptid : "";
	}

	/**
	 * 获取 发票信息编号，UUID
	 * @return	 发票信息编号，UUID
	 */
	public String getReceiptid()
	{
		return this.receiptid;
	}

	/**
	 * 设置 公司名称 
	 * @param companyname	 公司名称 
	 */
	public void setCompanyname(String companyname)
	{
		this.companyname = companyname != null ? companyname : "";
	}

	/**
	 * 获取 公司名称 
	 * @return	 公司名称 
	 */
	public String getCompanyname()
	{
		return this.companyname;
	}

	/**
	 * 设置是否默认选项，Y-是，N-否
	 * @param isdefault	是否默认选项，Y-是，N-否
	 */
	public void setIsdefault(String isdefault)
	{
		this.isdefault = isdefault != null ? isdefault : "";
	}

	/**
	 * 获取是否默认选项，Y-是，N-否
	 * @return	是否默认选项，Y-是，N-否
	 */
	public String getIsdefault()
	{
		return this.isdefault;
	}
}